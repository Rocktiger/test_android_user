package user.test.com.test_android_user.bean;

public class AccountFlowBean {
    private String bankImg;
    private String bankNameNumber;
    private String bankNumMoney;
    private String bankDate;

    public String getBankImg() {
        return bankImg;
    }

    public void setBankImg(String bankImg) {
        this.bankImg = bankImg;
    }

    public String getBankNameNumber() {
        return bankNameNumber;
    }

    public void setBankNameNumber(String bankNameNumber) {
        this.bankNameNumber = bankNameNumber;
    }

    public String getBankNumMoney() {
        return bankNumMoney;
    }

    public void setBankNumMoney(String bankNumMoney) {
        this.bankNumMoney = bankNumMoney;
    }

    public String getBankDate() {
        return bankDate;
    }

    public void setBankDate(String bankDate) {
        this.bankDate = bankDate;
    }
}
